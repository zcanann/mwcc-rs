int difference_bytes(char *left, char *right)
{
    return left - right;
}

int difference_halfwords(short *left, short *right)
{
    return left - right;
}

int difference_words(int *left, int *right)
{
    return left - right;
}

int difference_doubles(double *left, double *right)
{
    return left - right;
}
